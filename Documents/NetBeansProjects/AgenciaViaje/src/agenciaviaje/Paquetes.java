
package agenciaviaje;

public class Paquetes {
    private String codigoPaquete;
    private String tipoPaquete;
    private String clasificacionPaquete;
    private String cantidadViajero;
    private String codigoTransporte;
    private String codigoHospedaje;
    private String codigoCrucero;
    private String codigoRentadora;
    private String codigoDestino;
    private String codigoLugar;
    private String costoTotalPasaje;

    public Paquetes(String codigoPaquete, String tipoPaquete, String clasificacionPaquete, String cantidadViajero, String codigoTransporte, String codigoHospedaje, String codigoCrucero, String codigoRentadora, String codigoDestino, String codigoLugar, String costoTotalPasaje) {
        this.codigoPaquete = codigoPaquete;
        this.tipoPaquete = tipoPaquete;
        this.clasificacionPaquete = clasificacionPaquete;
        this.cantidadViajero = cantidadViajero;
        this.codigoTransporte = codigoTransporte;
        this.codigoHospedaje = codigoHospedaje;
        this.codigoCrucero = codigoCrucero;
        this.codigoRentadora = codigoRentadora;
        this.codigoDestino = codigoDestino;
        this.codigoLugar = codigoLugar;
        this.costoTotalPasaje = costoTotalPasaje;
    }

    public String getCodigoPaquete() {
        return codigoPaquete;
    }

    public void setCodigoPaquete(String codigoPaquete) {
        this.codigoPaquete = codigoPaquete;
    }

    public String getTipoPaquete() {
        return tipoPaquete;
    }

    public void setTipoPaquete(String tipoPaquete) {
        this.tipoPaquete = tipoPaquete;
    }

    public String getClasificacionPaquete() {
        return clasificacionPaquete;
    }

    public void setClasificacionPaquete(String clasificacionPaquete) {
        this.clasificacionPaquete = clasificacionPaquete;
    }

    public String getCantidadViajero() {
        return cantidadViajero;
    }

    public void setCantidadViajero(String cantidadViajero) {
        this.cantidadViajero = cantidadViajero;
    }

    public String getCodigoTransporte() {
        return codigoTransporte;
    }

    public void setCodigoTransporte(String codigoTransporte) {
        this.codigoTransporte = codigoTransporte;
    }

    public String getCodigoHospedaje() {
        return codigoHospedaje;
    }

    public void setCodigoHospedaje(String codigoHospedaje) {
        this.codigoHospedaje = codigoHospedaje;
    }

    public String getCodigoCrucero() {
        return codigoCrucero;
    }

    public void setCodigoCrucero(String codigoCrucero) {
        this.codigoCrucero = codigoCrucero;
    }

    public String getCodigoRentadora() {
        return codigoRentadora;
    }

    public void setCodigoRentadora(String codigoRentadora) {
        this.codigoRentadora = codigoRentadora;
    }

    public String getCodigoDestino() {
        return codigoDestino;
    }

    public void setCodigoDestino(String codigoDestino) {
        this.codigoDestino = codigoDestino;
    }

    public String getCodigoLugar() {
        return codigoLugar;
    }

    public void setCodigoLugar(String codigoLugar) {
        this.codigoLugar = codigoLugar;
    }

    public String getCostoTotalPasaje() {
        return costoTotalPasaje;
    }

    public void setCostoTotalPasaje(String costoTotalPasaje) {
        this.costoTotalPasaje = costoTotalPasaje;
    }
    
    
    
}
